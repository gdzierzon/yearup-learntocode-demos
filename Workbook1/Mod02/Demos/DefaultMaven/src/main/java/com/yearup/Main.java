package com.yearup;

public class Main
{
    public static void main(String[] args)
    {
        System.out.println();

        String name; // String is one or more characters together
        name = "Mary";

        char c = 'A'; // char is a single character

        // whole
        int age = 23; // 32 bit integer -2.2b - 2.2b
        long peopleInTheWorld = 3000000000L; // 64 bit integer
        short sixteenBitInt;
        byte eightBitInt;

        boolean hasValue; // store either true or false

        // floating point values
        float distanceToTheMoon = 12.5F; // 32 bit floating point
        double distanceToTheSun = 12.5; // 64 bit
    }

}
